const languageSelectors = document.getElementsByClassName("lang-link");

Array.from(languageSelectors).forEach((languageSelector) => {
  languageSelector.addEventListener("click", (e) => {
    var langTypeElement = e.target.querySelector(".lang-type");

    if (langTypeElement.textContent === "AR") {
      langTypeElement.textContent = "EN";
    } else {
      langTypeElement.textContent = "AR";
    }

    setLang(langTypeElement.textContent);
    localStorage.setItem("lang", langTypeElement.textContent);
  });
});


document.addEventListener("DOMContentLoaded",()=>{
    const language = localStorage.getItem("lang");
    setLang(language);
})

const setLang = (language) =>{
    const elements = document.querySelectorAll("[data-translation]");
    elements.forEach((el) =>{
        const translationKey = el.getAttribute('data-translation');
        el.textContent = translations[language][translationKey];
        el.classList.remove("arabic-font", "english-font");
        if (language === "AR") {
          el.classList.add("arabic-font");
        } else {
          el.classList.add("english-font");
        }
    });
    document.dir = language === "AR"? "rtl":"EN";
}